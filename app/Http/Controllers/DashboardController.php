    {
        $substitution = Usersubstitutions::where([
            ['user_id', '=', Auth::user()->id],
            ['date_to', '>=', date("dd-mm-yyyy")]
        ])->first();

        if($substitution){
            Auth::user()->store = $substitution->store_id;
        }

        return view('admin.selling.index');
    }

